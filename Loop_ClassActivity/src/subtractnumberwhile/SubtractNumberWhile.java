package subtractnumberwhile;

public class SubtractNumberWhile {
	// write a while loop ... while a number is greater than 0 subtract 1 from the
	// number starting at 10.
	public static void main(String[] args) {
		int number = 10;
		while(number > 0) {
			number--;
			System.out.println(number);		
		}
		
	}

}
