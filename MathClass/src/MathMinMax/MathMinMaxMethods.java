package MathMinMax;

public class MathMinMaxMethods {

	public static void main(String[] args) {
		
		int a =10, b = -20;
		
		System.out.println(Math.abs(a));
		System.out.println("Absolute value of -20 is: " +Math.abs(b));
		System.out.println("The maximum number from 10 and -20 is: " + Math.max(a, b));
		System.out.println("The minimum number from 10 and -20 is: " +Math.min(a, b));
		

	}

}
